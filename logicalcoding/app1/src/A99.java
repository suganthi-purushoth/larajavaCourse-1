class A99 
{
	public static void main(String[] args) 
	{
		for (char i='a';i<='f' ;i++ )
		{
			for (char j='a';j<=i ;j++ )
			{
				System.out.print(j + " ");
			}
			System.out.println();
		}
		for (char i='e';i>='a' ;i-- )
		{
			for (char j='a';j<=i ;j++ )
			{
				System.out.print(j +" ");
			}
			System.out.println();
		}
	}
}
