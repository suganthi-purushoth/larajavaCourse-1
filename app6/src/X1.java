class X1 
{
	public static void main(String[] args) 
	{
		System.out.println(Integer.toBinaryString(43));
		System.out.println(Integer.toBinaryString(69));
		System.out.println(43 | 69);  //  bitwise or
		System.out.println(43 & 69);  //  bitwise and
		System.out.println(43 ^ 69);  //  bitwise xor
	}
}
